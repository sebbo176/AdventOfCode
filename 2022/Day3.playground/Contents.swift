struct Rucksack {

    init(_ input: String) {
        let data = stride(from: 0, to: input.count, by: input.count / 2)
            .map { input[input.index(input.startIndex, offsetBy: $0) ..< input.index(input.startIndex, offsetBy: min($0 + input.count / 2, input.count))] }
        self.firstPart = String(data[0])
        self.lastPart = String(data[1])
    }

    var firstPart: String
    var lastPart: String

    var duplicate: Character {
        for char in firstPart {
            if let duplicate = lastPart.first(where: { $0 == char }) {
                return duplicate
            }
         }
        fatalError()
    }

    var valueOfDuplicate: Int {
        allLetters.firstIndex(of: duplicate)! + 1
    }
}

